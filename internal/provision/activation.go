package provision

import (
	"context"
	"fmt"

	incuscli "github.com/lxc/incus/v6/client"
	"github.com/gschlager/silo/internal/incus"
)

// systemActivationFile bootstraps the per-user activation file for bash login
// shells. Fedora's /etc/profile sources /etc/profile.d/*.sh, so every bash login
// shell (setup, run, daemons, enter) picks it up.
const systemActivationFile = "/etc/profile.d/silo.sh"

// UserActivationFile is the per-user, user-writable file sourced by every login
// shell. PATH tweaks, env: exports, and tool activations (rv, mise, …) all live
// here so they work for bash and zsh alike and are picked up by daemons and
// non-interactive `silo run` — unlike ~/.zshrc, which is interactive-only.
// Project setup and presets append to it without sudo.
const UserActivationFile = "$HOME/.silo/env.sh"

// setupActivation creates the shell-neutral activation seam for the dev user:
// a user-writable ~/.silo/env.sh (seeded with the ~/.local/bin PATH tweak) plus
// the bootstraps that source it. bash login shells source it through
// /etc/profile.d/silo.sh; zsh through a guarded line in ~/.zshenv (sourced in
// every zsh mode, including non-interactive daemons).
//
// Non-interactive non-login bash (agent Bash tool calls, scripts, git hooks)
// reads no startup file at all unless BASH_ENV is set, so the seed exports
// BASH_ENV pointing back at env.sh. Every silo session starts from a login
// shell that sources env.sh, so all descendant bash shells inherit BASH_ENV
// and re-source it — re-running directory-sensitive activations like
// `rv shell env` for their own working directory.
func setupActivation(ctx context.Context, server incuscli.InstanceServer, container, username, shell string) error {
	rootOpts := incus.ExecOpts{}
	userOpts := incus.ExecOpts{User: 1000, Home: "/home/" + username}
	homeDir := "/home/" + username

	// Per-user activation file (user-writable so setup/presets can append).
	if _, err := incus.Exec(ctx, server, container, userOpts, []string{
		"sh", "-c", "mkdir -p ~/.silo",
	}); err != nil {
		return fmt.Errorf("creating ~/.silo: %w", err)
	}
	// The PATH prepend is guarded because env.sh is re-sourced by every
	// non-interactive bash via BASH_ENV, which would otherwise stack duplicates.
	seed := `export BASH_ENV="$HOME/.silo/env.sh"
case ":$PATH:" in *":$HOME/.local/bin:"*) ;; *) export PATH="$HOME/.local/bin:$PATH" ;; esac
`
	if _, err := incus.ExecWithStdin(ctx, server, container, userOpts, []string{
		"tee", homeDir + "/.silo/env.sh",
	}, []byte(seed)); err != nil {
		return fmt.Errorf("seeding activation file: %w", err)
	}

	// bash bootstrap: source the per-user file from /etc/profile.d.
	bootstrap := "# Generated by silo — do not edit.\n" +
		`[ -f "$HOME/.silo/env.sh" ] && . "$HOME/.silo/env.sh"` + "\n"
	if _, err := incus.ExecWithStdin(ctx, server, container, rootOpts, []string{
		"tee", systemActivationFile,
	}, []byte(bootstrap)); err != nil {
		return fmt.Errorf("writing %s: %w", systemActivationFile, err)
	}

	// zsh bootstrap: zsh does not read /etc/profile in every mode, so source the
	// per-user file from ~/.zshenv (sourced in all zsh modes).
	if shell == "zsh" {
		if _, err := incus.Exec(ctx, server, container, userOpts, []string{
			"sh", "-c", `printf '%s\n' '[ -f "$HOME/.silo/env.sh" ] && . "$HOME/.silo/env.sh"' >> ~/.zshenv`,
		}); err != nil {
			return fmt.Errorf("writing ~/.zshenv: %w", err)
		}
	}
	return nil
}

// appendActivation appends content to the per-user activation file as the dev
// user. Used to record env: exports during provisioning.
func appendActivation(ctx context.Context, server incuscli.InstanceServer, container, username, content string) error {
	userOpts := incus.ExecOpts{User: 1000, Home: "/home/" + username}
	if _, err := incus.ExecWithStdin(ctx, server, container, userOpts, []string{
		"tee", "-a", "/home/" + username + "/.silo/env.sh",
	}, []byte(content)); err != nil {
		return fmt.Errorf("appending to activation file: %w", err)
	}
	return nil
}
